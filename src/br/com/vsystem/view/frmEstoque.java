    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        txtpesquisa.setText("");
        ProdutoDAO prodao = new ProdutoDAO();
        List<ProdutoModel> lista = prodao.listarTodosProdutos();

        DefaultTableModel dados = (DefaultTableModel) tabelaProduto.getModel();
        
        //teste

        dados.setNumRows(0);

        for (ProdutoModel p : lista) {
            dados.addRow(new Object[]{
                p.getProduto_id(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtd_estoque(),
                p.getFornecedor().getNome_fantasia()
            });

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

                qtd_inicial = Integer.parseInt(txtestoque.getText());
                qtd_adicionada = Integer.parseInt(txtqtdadicional.getText());
                qtd_nova = qtd_inicial + qtd_adicionada;
                
                
                frmAlteracaoEstoque telaAlteracaoEstoque = new frmAlteracaoEstoque();
                telaAlteracaoEstoque.id_produto = id_produto;
                telaAlteracaoEstoque.qtd_inicial = qtd_inicial;
                telaAlteracaoEstoque.qtd_adicionada = qtd_adicionada;
                telaAlteracaoEstoque.qtd_nova = qtd_nova;
                telaAlteracaoEstoque.setVisible(true);
          
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void txtpesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyPressed
        String nome = "%" + txtpesquisa.getText() + "%";

        ProdutoDAO cliDAO = new ProdutoDAO();
        
        List<ProdutoModel> lista = cliDAO.PesquisarPorDescricao(nome);

        DefaultTableModel dados = (DefaultTableModel) tabelaProduto.getModel();

        dados.setNumRows(0);

        for (ProdutoModel p : lista) {
            dados.addRow(new Object[]{
                p.getProduto_id(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtd_estoque(),
                p.getFornecedor().getNome_fantasia()

            });

        }
    }//GEN-LAST:event_txtpesquisaKeyPressed

